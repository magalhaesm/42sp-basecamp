int	ft_sqrt(int nb)
{
	int	sqrt;

	sqrt = 1;
	if (nb == 1)
		return (1);
	if (nb <= 0)
		return (0);
	while (nb / sqrt > sqrt)
		sqrt++;
	if (nb / sqrt == sqrt && nb % sqrt == 0)
		return (sqrt);
	return (0);
}
